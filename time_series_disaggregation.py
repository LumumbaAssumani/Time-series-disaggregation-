def remove_outliers (data , std):
  Q1 = np.percentile(data, 25)
  Q3 = np.percentile(data, 75)
  IQR = Q3 - Q1
  upper_threshold = Q3 + (std * IQR)
  cleaned = data.copy()
  cleaned[(data < 0) | (data > upper_threshold)] = np.nan
  return cleaned

