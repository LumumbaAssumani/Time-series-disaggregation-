def fix_timestamp(val):
    if str(val).replace('.', '').isdigit():
        # Excel serial number (midnight rows) → convert to proper datetime
        return pd.Timestamp('1899-12-30') + pd.to_timedelta(float(val), unit='D')
    else:
        # Normal date string → parse as DD/MM/YYYY HH:MM
        return pd.to_datetime(val, dayfirst=True)

