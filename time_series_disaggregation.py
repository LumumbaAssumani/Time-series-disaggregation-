# (This assigns D's daily total to all 48 half-hours in that specific day)
df_d_disagg['D_Daily_Target'] = df_d_disagg['Date'].map(D_clean)

# 6. THE MATH: Proportional Disaggregation
# Safely handle days where THD sum might be 0 to avoid dividing by zero errors
safe_divisor_d = df_d_disagg['THD_Daily_Sum'].replace(0, np.nan)

# Formula: (Half-Hour THD / Daily THD Sum) * Daily Target D
df_d_disagg['D_HH_Estimated'] = (df_d_disagg['THD_HH'] / safe_divisor_d) * df_d_disagg['D_Daily_Target']

# If the network was off (NaN from above), set the estimate to 0
df_d_disagg['D_HH_Estimated'] = df_d_disagg['D_HH_Estimated'].fillna(0)

